/*
    Lo usamos para comparar valores
*/

console.log(5 == 5); // es verdadero si ambos valores son iguales, mostrando un valor boleano.
console.log(5 == 4); // es falso si ambos valores son distintos, mostrando un valor boleano.

// De igual manera podemos comparar cadenas de caracteres.
console.log('Hola' == 'Hola'); // nos da como resultado true, es verdadera la expresion
console.log('Hola' == 'JavaScript'); // Si la comparamos con cualquier otro valor el resultado sera falso.